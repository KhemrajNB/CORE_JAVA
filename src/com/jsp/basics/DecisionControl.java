package com.jsp.basics;

public class DecisionControl {

	public static void main(String[] args) {
		
		// While Loop
		int i = 1;
		while(i<=5) {
			System.out.println("Hello World");
			i++;
		}
		
		
		//do while loop
		i=1;
		do {
			System.out.println("HELLO");
			i++;
		}while(i<=5);
		
		
		//for loop
		for(int j = 1;j<=5;j++) {
			System.out.println("Hii");
		}

	}

}
